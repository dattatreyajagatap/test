        $('#name').on('keypress', function (event) {
            var regex = new RegExp("^[a-zA-Z0-9]+$");
            var key = String.fromCharCode(!event.charCode ? event.which : event.charCode);
            if (!regex.test(key)) {
            event.preventDefault();
            alert("only alphanumeric is allowed")
            return false;
            }
        });
        $('#mobile').on('blur',function(e) {
            var mobile = $('#mobile').val();
            if(mobile.length < 10){
                
                $('.submit').hide();   
                alert("Eneter 10 digit")
            }else{
                $('.submit').show();
            }
            
        });

        $('#dob').change(function () {
            var now = new Date();   //Current Date
            var past = new Date($('#dob').val());  //Date of Birth
            var nowYear = now.getFullYear();  //Get current year
            var pastYear = past.getFullYear();//Get Date of Birth year
            var age = nowYear - pastYear;  //calculate the difference
            if(age <= 17){
                
                $('.submit').hide();   
                alert(" Age must be above 18yr")
            }else{
                $('.submit').show();
            }

        });
